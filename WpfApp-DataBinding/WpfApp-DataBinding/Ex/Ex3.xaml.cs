namespace WpfApp2_DataBinding1.Ex
{
    /// <summary>
    /// Interaction logic for Ex3.xaml
    /// </summary>
    public partial class Ex3 : UserControl, INotifyPropertyChanged
    {
        private string _name;

        public string Name
        {
            get => _name;
            set
            {
                if (_name != value)
                {
                    _name = value;
                    OnPropertyChanged(nameof(Name)); // Notify UI about property change
                }
            }
        }

        public Ex3()
        {
            InitializeComponent();
            DataContext = this;
        }

        public event PropertyChangedEventHandler PropertyChanged;

        protected virtual void OnPropertyChanged(string propertyName)
        {
            PropertyChanged?.Invoke(this, new PropertyChangedEventArgs(propertyName));
        }
    }
}
